#연습1
b = "Hello, World!"
print(b[2:5])
#[a:b]a 부터 시작에서 b(는 제외)앞까지의 문자를 출력

#연습2
b = "Hello, World!"
print(b[:5])
#시작을 생략하면 문자열 처음(인덱스0)부터 시작

#연습3
b = "Hello, World!"
print(b[2:])
#종료점을 생략하면 문자 끝까지.

#연습4
b = "Hello, World!"
print(b[-5:-2])
#네거티브(-) 사용시 뒤에서부터 시작 *뒤에서 부터 할 때는 0이 시작이 아니라 1이 시작*